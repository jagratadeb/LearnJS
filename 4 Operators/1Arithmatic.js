let a = 5;
let b = 5;

console.log("Arithmetic Operators");

// Addition
console.log("a + b =", a + b); // 10

// Subtraction
console.log("a - b =", a - b); // 0

// Division
console.log("a / b =", a / b); // 1

// Multiplication
console.log("a * b =", a * b); // 25

// Exponentiation
console.log("a ** b =", a ** b); // 3125

// Modulus
console.log("a % b =", a % b); // 0

// Post-increment
console.log("a++ =", a++); // 5
console.log("After a++: a =", a); // 6

// Post-decrement
console.log("a-- =", a--); // 6
console.log("After a--: a =", a); // 5

// Pre-increment
console.log("++a =", ++a); // 6

// Pre-decrement
console.log("--a =", --a); // 5









