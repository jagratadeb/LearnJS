let a = 5;
let b = 5;

// Post-increment
console.log("a++ =", a++); // 5
console.log("After a++: a =", a); // 6

// Post-decrement
console.log("a-- =", a--); // 6
console.log("After a--: a =", a); // 5

// Pre-increment
console.log("++a =", ++a); // 6

// Pre-decrement
console.log("--a =", --a); // 5
