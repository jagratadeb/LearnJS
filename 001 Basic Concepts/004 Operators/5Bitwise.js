x = 5;  // 0101 in binary
y = 3;  // 0011 in binary

console.log("Bitwise Operators");

// AND
console.log("x & y :", x & y); // 1

// OR
console.log("x | y :", x | y); // 7

// XOR
console.log("x ^ y :", x ^ y); // 6

// NOT
console.log("~x :", ~x); // -6

// Left shift
console.log("x << 1 :", x << 1); // 10

// Right shift
console.log("x >> 1 :", x >> 1); // 2

// Zero-fill right shift
console.log("x >>> 1 :", x >>> 1); // 2
