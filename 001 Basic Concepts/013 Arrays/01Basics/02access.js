// Example: Accessing elements in an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Accessing elements - getting a specific element from an array
console.log("First element of arr1:", arr1[0]); // Output: 1
console.log("Last element of arr1:", arr1[arr1.length - 1]); // Output: 5

// Loop through the array and print each element
for (let i = 0; i < arr1.length; i++) {
    console.log(arr1[i]);
}
