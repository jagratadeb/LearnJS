// Example: Using slice() to extract a portion of an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Print the original array
console.log("Original Array:", arr1);

// Slice - extracting a portion of the array (from index 1 to 3, not including 3)
console.log("Slice of arr1 (1, 3):", arr1.slice(1, 3)); // Output: [2, 3]
