// Example: Using shift() to remove the first element from an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Shift - removing the first element from the array
let shiftedElement = arr1.shift(); // removes 1

// Print the removed element and the updated array
console.log("Shifted Element:", shiftedElement); // Output: 1
console.log("Array after shift:", arr1); // Output: [2, 3, 4, 5]
