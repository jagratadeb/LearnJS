// Example: Using pop() to remove the last element from an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Pop - removing the last element from the array
let poppedElement = arr1.pop(); // removes 5

// Print the removed element and the updated array
console.log("Popped Element:", poppedElement); // Output: 5
console.log("Array after pop:", arr1); // Output: [1, 2, 3, 4]
