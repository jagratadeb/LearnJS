// Example: Using unshift() to add elements to the beginning of an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Unshift - adding an element to the beginning of the array
arr1.unshift(0); // arr1 is now [0, 1, 2, 3, 4, 5]

// Print the updated array
console.log("Array after unshift:", arr1); // Output: [0, 1, 2, 3, 4, 5]
