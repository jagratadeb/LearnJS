// Example: Using reverse() to reverse the order of elements in an array in JavaScript

// Declare an array of fruits
let fruits = ["apple", "mango", "banana", "orange"];
console.log("Original array:", fruits);

// Reverse - reverse the order of elements in the array
let reverse = fruits.reverse();

// Print the reversed array
console.log("Reversed array:", fruits); // Output: ["orange", "banana", "mango", "apple"]
console.log("Return value of reverse():", reverse); // Output: ["orange", "banana", "mango", "apple"]