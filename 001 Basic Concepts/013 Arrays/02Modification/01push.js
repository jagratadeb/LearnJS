// Example: Using push() to add elements to an array in JavaScript

// Declare an array with 5 elements
let arr1 = [1, 2, 3, 4, 5];

// Push - adding an element to the end of the array
arr1.push(6); // arr1 is now [1, 2, 3, 4, 5, 6]

// Print the updated array
console.log("Array after push:", arr1); // Output: [1, 2, 3, 4, 5, 6]