// Example: Using map() to create a new array by transforming each element

// Declare an array of numbers
let arr1 = [1, 2, 3, 4, 5];

// Map - create a new array by adding 1 to each element
let mappedArray = arr1.map(x => x + 1); // [2, 3, 4, 5, 6]

// Print the new mapped array
console.log("Mapped Array:", mappedArray); // Output: [2, 3, 4, 5, 6]
