// Example: Using includes() to check if an array contains a value

// Declare an array of fruit names
let fruits = ["apple", "mango", "banana", "orange"];

// includes - check if 'mango' is in the array
let trueOrFalse = fruits.includes("mango"); // true

// Print the result
console.log("Does the array include 'mango'?", trueOrFalse); // Output: true