// Example: Using filter() to create a new array with elements that pass a test

// Declare an array of numbers
let arr1 = [1, 2, 3, 4, 5];

// Filter - create a new array with elements greater than 3
let filteredArray = arr1.filter(x => x > 3); // [4, 5]

// Print the filtered array
console.log("Filtered Array:", filteredArray); // Output: [4, 5]
