// Example: Using find() to get the first element that satisfies a condition

// Declare an array of numbers
let arr = [1, 2, 3, 4, 5];

// Find - get the first element greater than 3
let foundElement = arr.find(ele => ele > 3); // 4

// Print the found element
console.log("First element greater than 3:", foundElement); // Output: 4