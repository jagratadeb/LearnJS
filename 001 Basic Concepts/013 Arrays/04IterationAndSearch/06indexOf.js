// Example: Using indexOf() to find the index of elements in an array

// Declare an array of fruit names
let fruits = ["apple", "mango", "banana", "orange"];

// indexOf - get the index of each fruit in the array
let appleIndex = fruits.indexOf("apple"); // 0
console.log("Index of 'apple':", appleIndex);

let mangoIndex = fruits.indexOf("mango"); // 1
console.log("Index of 'mango':", mangoIndex);

let bananaIndex = fruits.indexOf("banana"); // 2
console.log("Index of 'banana':", bananaIndex);

let orangeIndex = fruits.indexOf("orange"); // 3
console.log("Index of 'orange':", orangeIndex);