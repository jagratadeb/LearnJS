// Initialize an array with three elements
let arr = ["Thomas", "Chad", "Brad"];

// Destructure the array into variables
let [item1, item2, item3, item4] = arr;

// Log the values of the variables to the console
console.log(item1); // Output: Thomas
console.log(item2); // Output: Chad
console.log(item3); // Output: Brad
console.log(item4); // Output: undefined

// Array destructuring is a convenient way to extract values from arrays and assign them to variables.
// It allows us to unpack elements from arrays into distinct variables in a concise and readable manner.
